  def lookup_for_file(path)
    return "text/plain" if WELL_KNOW_TXT_FILE.include?(File.basename(path).downcase)
    suffix1 = (/\.(\w+)$/ =~ path && $1.downcase)
    suffix2 = (/\.(\w+)\.[\w\-]+$/ =~ path && $1.downcase)
    MIME_TYPES[suffix1] || MIME_TYPES[suffix2] || "application/octet-stream"
  end
  
